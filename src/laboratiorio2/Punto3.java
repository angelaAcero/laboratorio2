    public int[][] burbuja (int matriz[][],int col)
    {
        for (int k = 1; k < matriz.length-1; k++)
        {
            for (int j = 0; j < matriz.length-k; j++) 
            {
                if (matriz[j][col] > matriz[j+1][col]) 
                {
                    int temp;
                    temp = matriz[j][col] ;
                    matriz[j][col] = matriz[j+1][col] ;
                    matriz[j+1][col] = temp;
                    
                    for (int i = 0; i < matriz[0].length; i++) {
                        if (1!=col)
                        {
                            
                            temp = matriz[j][i] ;
                            matriz[j][i] = matriz[j+1][i] ;
                            matriz[j+1][i] = temp;
                        }
                        
                    }
                    
                }
            }
        }
        return matriz;
    }

